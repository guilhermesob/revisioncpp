#include<iostream>
#include<string>

using namespace std;
int main(){
	int a = 10;
	int b = 20;
	cout<<(a-b);
	cout<<endl;
	cout<<(a+b);
	cout<<endl;
	cout<<(a/b);
	cout<<endl;
	cout<<(a*b);
	cout<<endl;
	cout<<(a%b);
	cout<<endl;

	cout<<"Comparision Operators";
	cout<<endl;
	cout<<(a>b);
	cout<<endl;
	cout<<(a<b);
	cout<<endl;
	cout<<(a==b);
	cout<<endl;
	cout<<(a!=b);
	cout<<endl;
	cout<<(a>=b);
	cout<<endl;
	cout<<(a<=b);

  // and  or  not 
	cout<<"Logical Operators";
	cout<<endl;
	cout<<(true && true);
	cout<<endl; 
	cout<<(true && false);
	cout<<endl; 
	cout<<(true || false); 
	cout<<endl;
	cout<<(!false);
	cout<<endl;
	cout<<(--a);
	
		
}
